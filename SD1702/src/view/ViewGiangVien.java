    public void showDataTable(List<GiangVien> lists) {
        tableModel.setRowCount(0);
        for (GiangVien giangVien : lists) {
            tableModel.addRow(giangVien.toRowData());
        }
    }

    public void showDetail(int show) {
        GiangVien giangVien = list.get(show);
        txtID.setText(giangVien.getId());
        txtTen.setText(giangVien.getTenGV());
        cboBac.setSelectedItem(String.valueOf(giangVien.getBac()));
        txtNamSinh.setText(String.valueOf(giangVien.getNamSinh()));
        txtDiaChi.setText(giangVien.getDiaChi());
        boolean gt = giangVien.isGioiTinh();
        if (gt == true) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
    }

    public GiangVien getFormData() {
        String id = String.valueOf(sr.getAll().size());
        String ten = txtTen.getText();
        boolean gt = rdoNam.isSelected();
        int bac = Integer.parseInt((String) cboBac.getSelectedItem());
        int namSinh = Integer.parseInt(txtNamSinh.getText());
        String diachi = txtDiaChi.getText();
        GiangVien giangVien = new GiangVien(id, ten, gt, bac, namSinh, diachi);
        return giangVien;
    }

